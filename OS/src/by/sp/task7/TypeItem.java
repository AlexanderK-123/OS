package by.sp.task7;

class TypeItem {
	String type;
	String description;

	TypeItem(String type, String description) {
		this.type = type;
		this.description = description;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getDescription() {
		return description;
	}
}
